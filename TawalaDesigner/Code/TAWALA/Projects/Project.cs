        private FormList createCombinedFormList()
        {
            var combinedForms = new FormList();
            var localDataSourceNames = new Collection<string>();

            foreach (IForm form in FormList)
            {
                combinedForms.Add(form);
                if (form.IsDataSource)
                {
                    localDataSourceNames.Add(form.DataSourceName);
                }
            }

            foreach (IForm externalForm in FieldProviders.ExternalForms)
            {
                if (!localDataSourceNames.Contains(externalForm.Name))
                {
                    combinedForms.Add(externalForm);
                }
            }

            return combinedForms;
        }

        public void MoveComponent(IProjectComponent component, int index)
        {
            if (component is IForm)
            {
                formList.EnableEvents = false;
                formList.Remove(component as IForm);
                formList.Insert(index, component as IForm);
                formList.EnableEvents = true;
            }
            if (component is IProcess)
            {
                processList.EnableEvents = false;
                processList.Remove(component as Process);
                processList.Insert(index, component as Process);
                processList.EnableEvents = true;
            }
            if (component is IDocument)
            {
                documentList.EnableEvents = false;
                documentList.Remove(component as IDocument);
                documentList.Insert(index, component as IDocument);
                documentList.EnableEvents = true;
            }

            allForms = null;
        }

