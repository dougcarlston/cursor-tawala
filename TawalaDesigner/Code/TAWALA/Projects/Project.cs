        /// </summary>
        /// <param name="name">
        /// document name to search for
        /// </param>
        /// <param name="createNew">
        /// if true, and if a document of the passed name does not already exist,
        /// one is created and added to the virtualDocumentList
        /// </param>
        public IDocument GetRealOrVirtualDocument(string name, bool createNew)
        {
            // see if the named document is in the "real" Document component list
            IDocument doc = documentList[name] ?? virtualDocumentList[name];

            // if not found, and if the flag is set to create a virtual document
            if (doc == null && createNew)
            {
                // create it and add it to the virtual doc list
                doc = ComponentMaker.MakeDocumentObject(name);
                virtualDocumentList.Add(doc);

                // update our conglomerate list of real and virtual documents
                updateRealOrVirtualDocumentList();
            }

            return doc;
        }

        /// <summary>
        /// updates the conglomerate list when a change is made to either of the others
        /// </summary>
        private void updateRealOrVirtualDocumentList()
        {
            Debug.Assert(realOrVirtualDocumentList != null);

            // clear the list
            realOrVirtualDocumentList.Clear();

            // add the documents from the real and virtual doc lists
            foreach (IDocument doc in documentList)
            {
                realOrVirtualDocumentList.Add(doc);
            }
            foreach (IDocument doc in virtualDocumentList)
            {
                realOrVirtualDocumentList.Add(doc);
            }

            Events.RaiseDocumentChangedEvent(new ComponentEventArgs(Document.NULL));
        }

