        public static ProjectFileOpenResult Open(string filePath)
        {
            long startTicks = Log.LogEnter("path = '{0}'", filePath);

            ProjectFileOpenResult result = ProjectFileOpenResult.InvalidFile;

            try
            {
                using (Stream stream = File.OpenRead(filePath))
                {
                    result = open(stream);
                }
            }
            catch (Exception e)
            {
                Log.LogException(e);
                throw;
            }

            Log.LogExit(startTicks);

            return result;
        }

        private static ProjectFileOpenResult open(Stream stream)
        {
            Events.RaiseOpeningProjectEvent();

            // slop accounts for possible binary bytes proceeding XML in prior versions which weren't pure XML.
            const int slop = 8;
            byte[] signature1 = Encoding.UTF8.GetBytes("<?xml version=\"1.0\" encoding=\"utf-8\"");
            byte[] signature2 = Encoding.UTF8.GetBytes("<?xml version=\"1.0\" encoding=\"UTF-8\"");
            var byteArray = new byte[signature1.Length + slop];

            // don't even bother in this case
            if (stream.Length < byteArray.Length)
            {
                return ProjectFileOpenResult.InvalidFile;
            }

            stream.Position = 0;
            stream.Read(byteArray, 0, byteArray.Length);
            stream.Position = 0;

            int matched = 0;
            int offset;

            for (offset = 0; offset < slop; ++offset)
            {
                for (matched = 0; matched < signature1.Length; ++matched)
                {
                    if (signature1[matched] != byteArray[matched + offset] && signature2[matched] != byteArray[matched + offset])
                    {
                        break;
                    }
                }

                if (matched == signature1.Length)
                {
                    break;
                }
            }

            if (matched != signature1.Length)
            {
                return ProjectFileOpenResult.InvalidFile;
            }

            if (offset == 0) // pure XML format (newer)
            {
                var converter = new TawalaProjectConverter(stream);
                if (!converter.ConvertXmlToProject())
                {
                    return ProjectFileOpenResult.NewerXmlFormat;
                }

                modifiedState.Reset();
            }
            else
            {
                string xmlString = new BinaryReader(stream, Encoding.UTF8).ReadString();

                using (var memoryStream = new MemoryStream())
                {
                    // make XML string into byte array
                    byte[] xmlByteArray = Encoding.UTF8.GetBytes(xmlString);

                    // write XML to memory stream
                    memoryStream.Write(xmlByteArray, 0, xmlByteArray.Length);

                    var converter = new TawalaProjectConverter(memoryStream);
                    converter.ConvertXmlToProject();

                    // Force modification state to always be true because of open from XML
                    modifiedState.ForceTrue();
                }
            }

            Events.RaiseProjectOpenedEvent(new ProjectEventArgs(current.projectName));

            // raise "current component set" as initially set to none/null
            projectEvents.RaiseCurrentComponentSetEvent(new ComponentEventArgs(null));

            return ProjectFileOpenResult.OK;
        }

        /// <summary>
        /// When saving to a file, use this method.
        /// </summary>
        public static void Save(string path)
        {
            long startTicks = Log.LogEnter("path = '{0}'", path);

            try
            {
                string oldName = Current.Name;
                string newName = Path.GetFileNameWithoutExtension(path);

                Current.Name = newName;

                using (Stream stream = File.Create(path))
                {
                    save(stream);
                }

                var args = new SaveProjectEventArgs(oldName, newName);

                projectEvents.RaiseSaveProjectEvent(args);

                modifiedState.Reset();
            }
            catch (Exception e)
            {
                Log.LogException(e);
                throw;
            }

            Log.LogExit(startTicks);
        }

        public static void SaveWithDebugInfo(string path, string debugInfo)
        {
            Save(path);

            using (Stream stream = File.OpenWrite(path))
            {
                stream.Seek(0, SeekOrigin.End);
                byte[] byteArray = Encoding.UTF8.GetBytes("\r\nDEBUG INFO:\r\n" + debugInfo);
                stream.Write(byteArray, 0, byteArray.Length);
            }
        }

        /// <summary>
        /// All Save operations should ultimately funnel through this function.
        /// If not, the data may not be synchronized with the UI!
        /// </summary>
        private static void save(Stream stream)
        {
            try
            {
                if (current != null)
                {
                    byte[] byteArray = Encoding.UTF8.GetBytes(current.ToXmlForSaving());
                    stream.Write(byteArray, 0, byteArray.Length);
                }
            }
            catch (Exception e)
            {
                Log.LogException(e);
                throw;
            }
        }

