                Current.globalFibItemStyle = element.GetChild("styles").GetAttribute("fibItemStyle");
                Current.globalMCItemStyle = element.GetChild("styles").GetAttribute("mcItemStyle");
                Current.globalTextItemStyle = element.GetChild("styles").GetAttribute("textItemStyle");
            }
        }

