    	private VariableList allVariables;
		public VariableList AllVariables
		{
			get
			{
				if (allVariables == null)
				{
					allVariables = new VariableList();
				}

				// kludge to reduce load time of large projects
				// jdf - 5/13/10
				if (LoadingProjectXml && allVariables.Count > 0)
				{
					return allVariables;
				}

				allVariables.Clear();
				foreach (Process proc in ProcessList)
				{
					foreach (Variable var in proc.Variables)
					{
						allVariables.AddUnique(var);
					}
				}

				foreach (IForm form in FormList)
				{
					foreach (IFormItem item in form.ItemList)
					{
						if (item is SkipInstructionsItem)
						{
							foreach (Variable var in ((SkipInstructionsItem)item).Instructions.Variables)
							{
								allVariables.AddUnique(var);
							}
						}
					}
				}

				allVariables.AddUnique(privateInvitationInviteeID);

				return allVariables;
			}
		}

